a = [1, 2, 3, 4, 5, 6]

print(a[:3]) # [1, 2, 3]

print(a[3:]) # [4, 5, 6]

print(a[1:5]) # [2, 3, 4, 5]

# 步长为2时，取值时会跳过一个元素
print(a[1:5:2]) # [2, 4]

# 步长为1时，等同于[1:5]，因为默认步长就是1
print(a[1:5:1])

# 未设置起始位置和结束位置，步长为2，等同于[0:6:2]
print(a[::2]) # [1, 3, 5]

print(a[::]) # [1, 2, 3, 4, 5, 6]

print(a[1:6:]) # [2, 3, 4, 5, 6]

print(a[-3]) # 4

print(a[0:-3]) # [1, 2, 3]
